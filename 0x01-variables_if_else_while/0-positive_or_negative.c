#include <stdio.h>
/**
 * Description: main - Assigns a random number to the variable n each time it is executed.
 * Return: Always 0 (Success)
 */
int main(void)
{
int n;
if (n > 0)
{
printf("%d is positive\n", n);
}
if (n < 0)
{
printf("%d is negative\n", n);
}
if (n == 0) 
{
printf("%d is zero\n", n);
}
return (0);
}
